<h2>Přidejte se</h2>
<p>Portál Matematika pro každého je matematický portál, který sdružuje lidi se stejným zájmem, tedy matematikou. Tento portál prochází víceméně každý den aktualizací. Dohromady tvoříme tým. Chcete být i vy součástí týmu?</p>
<p>Umíte řešit rovnice? Nebo umíte nějakou jinou část matematiky? Rádi řešite matematické hříčky, hlavolamy nebo hádanky? Neváhejte a podělte se s ostatními o vaše znalosti.</p>
<p>Ať už děláte rádi cokoliv a týká se to matematiky, připojte se k našemu týmu a získejte nové, či rozšiřte vaše stávající zkušenosti.</p>
